import express from "express";
import bodyParser from "body-parser";

const app = express();
const port = 3000;
const masterKey = "FJS93JQ-I38FEK5-FPQN5WZ-PR57DDC";

app.use(bodyParser.urlencoded({ extended: true }));


//1. GET a random joke
app.get("/random", (req,res) => {
  const randomNum = Math.floor(Math.random()*jokes.length);
  res.json(jokes[randomNum]);
});


//2. GET a specific joke
app.get("/joke/:id", (req,res) => {
  const id = parseInt(req.params.id);
  const foundJoke = jokes.find((joke) => joke.id === id);
  if (foundJoke == undefined) {
    res
      .status(404)
      .json({error: `The joke with an id of ${id} does not exist`});
  } else {
    res.json(foundJoke);
  }
  
});

//3. GET a jokes by filtering on the joke type
app.get("/filter", (req,res) => {
  const type = req.query.type;
  const selectedType = jokes.filter((joke) => joke.jokeType === type);
  res.json(selectedType);
});

//4. POST a new joke
app.post("/joke", (req,res) => {
  const newJoke = {
    id: jokes.length + 1,
    jokeText: req.body.text,
    jokeType: req.body.type,
  };
  jokes.push(newJoke);
  res.json(newJoke);
});

//5. PUT a joke
app.put("/joke/:id", (req,res) => {
  const id = parseInt(req.params.id);
  const replacementJoke = {
    id: id,
    jokeText: req.body.text,
    jokeType: req.body.type
  }
  const foundJoke = jokes.findIndex((joke) => joke.id === id);
  if (foundJoke == undefined) {
    res
      .status(404)
      .json({error: "This id does not exist"});
  } else {
    jokes[foundJoke] = replacementJoke;
    res.json(replacementJoke);
  }
});

//6. PATCH a joke
app.patch("/joke/:id", (req,res) => {
  const id = parseInt(req.params.id);
  const foundJoke = jokes.find((joke) => joke.id === id);

  if (foundJoke == undefined) {
    res
      .status(404)
      .json({error: `The joke with id of ${id} does not exist.`});
  } else {
    const replacementJoke = {
      id: id,
      jokeText: req.body.text || foundJoke.jokeText,
      jokeType: req.body.type || foundJoke.jokeType
    }
    const jokeIndex = jokes.findIndex((joke) => joke.id === id);
    jokes[jokeIndex] = replacementJoke;
    res
      .json(jokes[jokeIndex]);
  }
});

//7. DELETE Specific joke
app.delete("/joke/:id", (req,res) => {
  const id = parseInt(req.params.id);
  const foundJoke = jokes.findIndex((joke) => joke.id === id);
  if (foundJoke == -1) {
    res
      .status(404)
      .json({error: `The joke with id of ${id} does not exist. No jokes where deleted`});
  } else {
    jokes.splice(foundJoke, 1);
    res.sendStatus(200);
  }
});

//8. DELETE All jokes
app.delete("/jokes/all", (req,res) => {
  const userKey = req.query.apiKey;
  if (userKey === masterKey) {
    jokes = [];
    res.sendStatus(200);
  } else {
    res
      .status(404)
      .json({error: "You are not authorized to perform this action"});
  }
});




app.listen(port, () => {
  console.log(`Successfully started server on port ${port}.`);
});

